void enemy_update()
{
    int dataNum = sizeof(enemyData) / sizeof(ENEMY_DATA);

    switch (enemy_state)
    {

    case 0:
        //////// 初期設定 ////////
        //sprEnemy = sprite_load(L"./Data/Images/enemy00.png");
        for (int i = 0; i < dataNum; ++i)
        {
            enemyData[i].sprEnemy = sprite_load(enemyData[i].filePath);
        }

        ++enemy_state;

    case 1:

         ++enemy_state;
        /*fallthrough*/

    case 2:
        //////// 通常時 ////////
        // 敵の行動
        player_check = false;


        for (int i = 0; i < ENEMY_MAX; ++i)
        {
            if (enemy[i].moveAlg == NULL)  continue;

            (*enemy[i].moveAlg)(&enemy[i]);
           
            //++enemy[i].timer;
        }

        if (stageNo == 4)
        {
            if (all_hasiracheck())  enemy_state++;
            enemy_timer = 0;
        }
        
        break;

    case 3:
        if (enemy_timer > 180)
            enemy_state = 10;

            break;
    }
    enemy_timer++;
}

