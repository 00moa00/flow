void enemy_moveX2(OBJ2D* obj)
{
    BG* bg = Game::instance()->bg();

    if (obj->scale.x > 0.0f)
    {
        obj->addSpeedX(-ENEMY_ACCEL_X);
    }
    else
    {
        obj->addSpeedX(ENEMY_ACCEL_X);
    }
    if (obj->speed.x > ENEMY_SPEED_X_MAX)
        obj->setSpeedX(ENEMY_SPEED_X_MAX);
    if (obj->speed.x < -ENEMY_SPEED_X_MAX)
        obj->setSpeedX(-ENEMY_SPEED_X_MAX);

    // X方向移動
    float oldX = obj->pos.x;
    obj->addPositionX(obj->speed.x);
    float deltaX = obj->pos.x - oldX;

    obj->kabeFlag = false;  // 壁フラグをfalseにしておく（プレイヤーでは使用しない）

    // 右方向あたり判定
    if (deltaX > 0)
    {
        if (bg->isWall(obj->pos.x + ENEMY_SIZE_X,
            obj->pos.y, ENEMY_SIZE_Y))
        {
            bg->mapHoseiRight(obj);     // 右方向の補正処理
            obj->kabeFlag = true;            // 壁フラグをtrueにしておく
            obj->scale.x *= -1.0f;
        }


    }

    // 左方向あたり判定
    if (deltaX < 0)
    {
        if (bg->isWall(obj->pos.x - ENEMY_SIZE_X,
            obj->pos.y, ENEMY_SIZE_Y))
        {
            bg->mapHoseiLeft(obj);
            obj->kabeFlag = true;
            obj->scale.x *= -1.0f;
        }
    }


    float dot = 0.0f;
    bool gakeFlag = false;

    if (obj->xFlip())
    {
        //左
        obj->addSpeedX(-ENEMY_ACCEL_X);
        obj->scale.x = 0.5f;
        dot = -ENEMY_SIZE_X;
    }
    else
    {
        //右
        obj->addSpeedX(ENEMY_ACCEL_X);
        obj->scale.x = -0.5f;
        dot = ENEMY_SIZE_X;
    }

    if (obj->onGround)
    {
        VECTOR2 iti = obj->pos + VECTOR2(dot, 1);
        if (bg->getTerrainAttr(iti) == BG::TR_ATTR::TR_NONE)
            gakeFlag = true;
    }

    //崖やあれや色々
    if (obj->kabeFlag==true && !player_check)
    {
        obj->setSpeedX(0.0f);
        obj->flip();
        obj->kabeFlag = true;
        gakeFlag = false;
    }

    else
    {
        if (gakeFlag && !player_check)
        {
            obj->setSpeedX(0.0f);
            obj->flip();
            obj->kabeFlag = false;
            gakeFlag = false;
        }
    }

}


