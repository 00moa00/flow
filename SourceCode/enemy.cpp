void enemy_anime(OBJ2D* obj, int koma)
{
    //animeTimerとanimeはOBH2D::setenemyの関数内ですべての変数がクリアされるので消える度にリセットされる


    if ((obj->animeTimer) % koma == 0) {

        obj->anime++;

        if (obj->anime > 6) {   //この7の数字は横画像の枚数
            obj->anime = 0;
        }
    }
    obj->texPos.x = obj->anime * obj->texSize.x;
    obj->animeTimer++;
}

