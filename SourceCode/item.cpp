void item_update()
{
    int dataNum = ARRAYSIZE(itemData);
    switch (item_state)
    {

    case 0:
        //////// 初期設定 ////////
        
        for (int i = 0; i < dataNum; ++i)
        {
            itemData[i].sprItem = sprite_load(itemData[i].filePath);
        }

        ++item_state;

        /*fallthrough*/

    case 1:
        //////// 通常時 ////////
        // アイテ?の行動
        for (int i = 0; i < ITEM_MAX; ++i)
        {
            if (item[i].moveAlg == NULL)  continue;

            (*item[i].moveAlg)(&item[i]);

         
        }
        item->timer++;
        break;
    }

}

