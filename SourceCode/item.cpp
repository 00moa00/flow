bool door_check(OBJ2D* obj, int diff)
{
    switch (obj->act)
    {
    case red:
        return ((diff >> 16) / 0xff==1);
    case blue:
        return ((diff >> 8) / 0xff == 1);
    case green:
        return (diff / 0xff ==1);
    }

    return false;
}

