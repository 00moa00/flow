bool BG::isFloor(float x, float y, float width) //x座標、y座標、width、画像の横幅
{
    for (; width > 0; width -= CHIP_SIZE)       // widthをCHIP_SIZE分減らしていく
    {
        const float left = x - width;
        const float right = x + width;

        // エリア?ェック
        if (left < 0)       continue;
        if (right >= WIDTH) continue;

        if (isHitDown(left, y))  return true;   // 左?から
        if (isHitDown(right, y)) return true;   // 右?から
    }
    return isHitDown(x, y);                     // 最後に?ん中で判定
}

